/// Convert an IR type string to a Python type annotation string.
///
/// This is used for `.pyi` stub generation.
fn ir_type_to_python_type(type_str: &str) -> String {
    let s = type_str.trim();

    match s {
        // Primitives
        "i8" | "i16" | "i32" | "i64" | "i128" | "isize"
        | "u8" | "u16" | "u32" | "u64" | "u128" | "usize" => "int".to_string(),
        "f32" | "f64" => "float".to_string(),
        "bool" => "bool".to_string(),
        "char" => "str".to_string(),
        "str" | "&str" => "str".to_string(),

        // Unit
        "()" => "None".to_string(),
        "!" => "typing.NoReturn".to_string(),

        // String
        "String" => "str".to_string(),

        // Self type
        "Self" => "Self".to_string(),

        // Reference types (strip the reference)
        _ if s.starts_with("&mut ") => {
            ir_type_to_python_type(&s[5..].trim())
        }
        _ if s.starts_with('&') => {
            ir_type_to_python_type(&s[1..].trim())
        }

        // Vec<T>
        _ if s.starts_with("Vec<") && s.ends_with('>') => {
            let inner = &s[4..s.len() - 1];
            format!("list[{}]", ir_type_to_python_type(inner))
        }

        // Option<T>
        _ if s.starts_with("Option<") && s.ends_with('>') && s.len() > 7 => {
            let inner = &s[7..s.len() - 1];
            let inner_py = ir_type_to_python_type(inner);
            if inner_py == "None" {
                "None".to_string()
            } else {
                format!("{} | None", inner_py)
            }
        }

        // Result<T, E>
        _ if s.starts_with("Result<") && s.ends_with('>') => {
            // Keep as Result<T, E> in stub — Python doesn't have native Result
            let inner = &s[7..s.len() - 1];
            // Split on first comma at top level for type args
            let parts = split_python_type_args(inner);
            if parts.len() >= 2 {
                format!(
                    "{}",
                    ir_type_to_python_type(parts[0].trim())
                )
            } else {
                "Any".to_string()
            }
        }

        // HashMap<K,V>
        _ if s.starts_with("HashMap<") && s.ends_with('>') => {
            let inner = &s[8..s.len() - 1];
            let parts = split_python_type_args(inner);
            if parts.len() >= 2 {
                format!(
                    "dict[{}, {}]",
                    ir_type_to_python_type(parts[0].trim()),
                    ir_type_to_python_type(parts[1].trim())
                )
            } else {
                "dict[Any, Any]".to_string()
            }
        }

        // HashSet<T>
        _ if s.starts_with("HashSet<") && s.ends_with('>') => {
            let inner = &s[8..s.len() - 1];
            format!("set[{}]", ir_type_to_python_type(inner))
        }

        // Box<T>
        _ if s.starts_with("Box<") && s.ends_with('>') => {
            let inner = &s[4..s.len() - 1];
            ir_type_to_python_type(inner)
        }

        // Tuple types
        _ if s.starts_with('(') && s.ends_with(')') => {
            let inner = &s[1..s.len() - 1];
            if inner.trim().is_empty() {
                "tuple[()]".to_string()
            } else {
                let parts: Vec<&str> = split_python_type_args(inner);
                let mapped: Vec<String> = parts
                    .iter()
                    .map(|p| ir_type_to_python_type(p.trim()))
                    .collect();
                if mapped.len() == 1 {
                    format!("tuple[{}]", mapped[0])
                } else {
                    format!("tuple[{}]", mapped.join(", "))
                }
            }
        }

        // dyn Trait
        _ if s.starts_with("dyn ") => "Any".to_string(),

        // impl Trait
        _ if s.starts_with("impl ") => "Any".to_string(),

        // Slice/array
        _ if s.starts_with('[') && s.ends_with(']') => {
            let inner = &s[1..s.len() - 1];
            if let Some(semi_pos) = inner.rfind("; ") {
                let elem = &inner[..semi_pos].trim();
                format!("list[{}]", ir_type_to_python_type(elem))
            } else if inner.trim().is_empty() {
                "list[Any]".to_string()
            } else {
                format!("list[{}]", ir_type_to_python_type(inner.trim()))
            }
        }

        // Generic named types
        _ if s.contains('<') && s.ends_with('>') => {
            // Try to parse as a named generic
            let angle_start = s.find('<').unwrap();
            let base = &s[..angle_start];
            base.to_string()
        }

        // Fallback: treat as a named type (struct/enum)
        _ => s.to_string(),
    }
}

/// Split type arguments at top-level commas for Python types.
fn split_python_type_args(s: &str) -> Vec<&str> {
    let mut args = Vec::new();
    let mut depth_angle = 0u32;
    let mut depth_paren = 0u32;
    let mut depth_bracket = 0u32;
    let mut start = 0usize;

    for (i, c) in s.char_indices() {
        match c {
            '<' => depth_angle += 1,
            '>' => depth_angle = depth_angle.saturating_sub(1),
            '(' => depth_paren += 1,
            ')' => depth_paren = depth_paren.saturating_sub(1),
            '[' => depth_bracket += 1,
            ']' => depth_bracket = depth_bracket.saturating_sub(1),
            ',' if depth_angle == 0 && depth_paren == 0 && depth_bracket == 0 => {
                args.push(&s[start..i]);
                start = i + 1;
            }
            _ => {}
        }
    }

    if start < s.len() {
        args.push(&s[start..]);
    }

    args
}

/// Extract Ok type from a Python-style `Result[T, E]` string.
fn extract_python_result_ok(type_str: &str) -> Option<String> {
    if type_str.starts_with("Result[") {
        let inner = &type_str[7..type_str.len() - 1];
        split_python_type_args(inner).first().map(|s| s.trim().to_string())
    } else {
        None
    }
}

