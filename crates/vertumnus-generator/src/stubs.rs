//! Python stub generation (.pyi and __init__.py) for Vertumnus bindings.
//!
//! Generates:
//! - `<package_name>.pyi` — Type stub file for IDE/type-checker support
//! - `python/<package_name>/__init__.py` — Thin re-export shim

use std::collections::HashMap;

use vertumnus_inspector::ir::{FieldVisibility, FunctionItem, IrItem, StructField};
use vertumnus_mapper::annotated_ir::{AnnotatedIr, AnnotatedItem, PyO3Strategy};



/// Generated Python stub output.
#[derive(Debug, Clone)]
pub struct GeneratedStubs {
    /// The `.pyi` file content
    pub pyi: String,
    /// The `__init__.py` file content
    pub init_py: String,
}

/// Generate the `.pyi` type stub file.
pub fn generate_pyi(
    annotated: &AnnotatedIr,
    _package_name: &str,
    methods_by_type: &HashMap<String, Vec<(FunctionItem, PyO3Strategy)>>,
) -> String {
    let mut pyi = String::new();

    // Header
    pyi.push_str("# Auto-generated by Vertumnus v0.1\n");
    pyi.push_str("# DO NOT EDIT MANUALLY. Changes will be overwritten on re-generation.\n");
    pyi.push_str(&format!(
        "# Type stubs for `{}` v{}\n\n",
        annotated.crate_name, annotated.crate_version
    ));

    // Imports
    pyi.push_str("from typing import Any, overload\n\n");

    // Separate items by kind so we output in order: functions → classes → enums
    let items: Vec<&AnnotatedItem> = annotated.items.iter().collect();
    let (functions, structs, enums, traits, _impls) = partition_map_by_kind(&items);

    // Free functions
    for item in &functions {
        if !is_exportable(&item.mapping.pyo3_strategy) {
            continue;
        }
        if let IrItem::Function(ref func) = item.original {
            pyi.push_str(&generate_function_stub(func, item));
        }
    }

    // Structs
    for item in &structs {
        if !is_exportable(&item.mapping.pyo3_strategy) {
            continue;
        }
        if let IrItem::Struct(ref s) = item.original {
            let methods = methods_by_type
                .get(&s.name)
                .map(|v| v.as_slice())
                .unwrap_or(&[]);
            pyi.push_str(&generate_struct_stub(s, methods, item));
        }
    }

    // Enums
    let has_c_like_enum = enums.iter().any(|item| {
        is_exportable(&item.mapping.pyo3_strategy)
            && matches!(&item.original, IrItem::Enum(e) if e.variants.iter().all(|v| v.fields.is_empty()))
    });
    if has_c_like_enum {
        pyi.push_str("from enum import IntEnum\n\n");
    }

    for item in &enums {
        if !is_exportable(&item.mapping.pyo3_strategy) {
            continue;
        }
        if let IrItem::Enum(ref e) = item.original {
            let methods = methods_by_type
                .get(&e.name)
                .map(|v| v.as_slice())
                .unwrap_or(&[]);
            pyi.push_str(&generate_enum_stub(e, methods, item));
        }
    }

    // Traits (informational)
    for item in &traits {
        pyi.push_str(&format!("# Trait '{}' — limited binding support\n\n", item.original.name()));
    }

    pyi
}

/// Generate the `__init__.py` re-export shim.
pub fn generate_init_py(annotated: &AnnotatedIr, package_name: &str) -> String {
    let mut init = String::new();

    init.push_str("# Auto-generated by Vertumnus v0.1\n");
    init.push_str("# DO NOT EDIT MANUALLY. Changes will be overwritten on re-generation.\n");
    init.push_str("# Thin Python shim — re-exports bindings from the native module.\n\n");

    init.push_str(&format!(
        "from .{} import (  # type: ignore\n",
        package_name
    ));

    // Collect all exportable names
    let mut names = Vec::new();
    for item in &annotated.items {
        match &item.original {
            IrItem::Function(f) => {
                if is_exportable(&item.mapping.pyo3_strategy) {
                    names.push(f.name.clone());
                }
            }
            IrItem::Struct(s) => {
                if is_exportable(&item.mapping.pyo3_strategy) {
                    names.push(s.name.clone());
                }
            }
            IrItem::Enum(e) => {
                if is_exportable(&item.mapping.pyo3_strategy) {
                    names.push(e.name.clone());
                }
            }
            _ => {}
        }
    }

    names.sort();
    for name in &names {
        init.push_str(&format!("    {},\n", name));
    }

    init.push_str(")\n\n");

    // Add a version attribute
    init.push_str(&format!(
        "__version__ = \"{}\"\n",
        annotated.crate_version
    ));

    init
}

/// Check if an item is exportable (not ManualStub-only).
fn is_exportable(strategy: &PyO3Strategy) -> bool {
    !matches!(strategy, PyO3Strategy::ManualStub)
}

