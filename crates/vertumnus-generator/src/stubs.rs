/// Partition a slice of annotated items by their IR item kind.
fn partition_map_by_kind<'a>(
    items: &'a [&'a AnnotatedItem],
) -> (
    Vec<&'a AnnotatedItem>,
    Vec<&'a AnnotatedItem>,
    Vec<&'a AnnotatedItem>,
    Vec<&'a AnnotatedItem>,
    Vec<&'a AnnotatedItem>,
) {
    let mut functions = Vec::new();
    let mut structs = Vec::new();
    let mut enums = Vec::new();
    let mut traits = Vec::new();
    let mut impls = Vec::new();

    for item in items {
        match &item.original {
            IrItem::Function(_) => functions.push(*item),
            IrItem::Struct(_) => structs.push(*item),
            IrItem::Enum(_) => enums.push(*item),
            IrItem::Trait(_) => traits.push(*item),
            IrItem::Impl(_) => impls.push(*item),
        }
    }

    (functions, structs, enums, traits, impls)
}

