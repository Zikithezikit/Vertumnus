/// Generate a Python stub for a free function.
fn generate_function_stub(func: &FunctionItem, item: &AnnotatedItem) -> String {
    let mut stub = String::new();

    // Doc comment
    if !func.doc.is_empty() {
        for line in func.doc.lines() {
            stub.push_str(&format!("# {}\n", line));
        }
    }

    // Manual stub warning
    if item.mapping.pyo3_strategy == PyO3Strategy::ManualStub {
        stub.push_str("# VERTUMNUS: manual binding required\n");
        for w in &item.mapping.warnings {
            stub.push_str(&format!("# WARNING: {}\n", w.message));
        }
        stub.push_str(&format!(
            "def {}(*args: Any, **kwargs: Any) -> Any: ...\n\n",
            func.name
        ));
        return stub;
    }

    // Async warning
    if func.is_async {
        stub.push_str("# NOTE: This function is async — use with an async runtime.\n");
        stub.push_str(&format!(
            "def {}(*args: Any, **kwargs: Any) -> Any: ...\n\n",
            func.name
        ));
        return stub;
    }

    // Build parameter list
    let params: Vec<String> = func
        .inputs
        .iter()
        .filter(|p| p.name != "self")
        .map(|p| {
            let py_type = ir_type_to_python_type(&p.type_str);
            format!("{}: {}", p.name, py_type)
        })
        .collect();

    // Build return type
    let return_type = if func.output.type_str == "()" {
        "None".to_string()
    } else if item.mapping.pyo3_strategy == PyO3Strategy::MapErr {
        // Result<T, E> — the return type is T (Err raises exception)
        let mapped = ir_type_to_python_type(&func.output.type_str);
        if mapped.starts_with("Result[") {
            // Extract T from Result[T, E]
            extract_python_result_ok(&mapped).unwrap_or(mapped)
        } else {
            mapped
        }
    } else {
        ir_type_to_python_type(&func.output.type_str)
    };

    stub.push_str(&format!(
        "def {}({}) -> {}: ...\n\n",
        func.name,
        params.join(", "),
        return_type
    ));

    stub
}

/// Generate a Python stub for a struct.
fn generate_struct_stub(
    s: &vertumnus_inspector::ir::StructItem,
    methods: &[(FunctionItem, PyO3Strategy)],
    item: &AnnotatedItem,
) -> String {
    let mut stub = String::new();

    // Doc comment
    if !s.doc.is_empty() {
        for line in s.doc.lines() {
            stub.push_str(&format!("# {}\n", line));
        }
    }

    // Manual stub warning
    if item.mapping.pyo3_strategy == PyO3Strategy::ManualStub {
        stub.push_str("# VERTUMNUS: manual binding required\n");
        for w in &item.mapping.warnings {
            stub.push_str(&format!("# WARNING: {}\n", w.message));
        }
        stub.push_str(&format!("class {}: ...\n\n", s.name));
        return stub;
    }

    stub.push_str(&format!("class {}:\n", s.name));

    // Public fields as annotations
    let public_fields: Vec<&StructField> = s
        .fields
        .iter()
        .filter(|f| f.visibility == FieldVisibility::Public)
        .collect();

    for field in &public_fields {
        let py_type = ir_type_to_python_type(&field.type_str);
        stub.push_str(&format!("    {}: {}\n", field.name, py_type));
    }

    if !public_fields.is_empty() {
        stub.push('\n');
    }

    // Methods
    for (method, strategy) in methods {
        let method_stub = generate_method_stub(method, strategy);
        for line in method_stub.lines() {
            stub.push_str("    ");
            stub.push_str(line);
            stub.push('\n');
        }
        stub.push('\n');
    }

    // If there are no public fields and no methods, add a placeholder
    if public_fields.is_empty() && methods.is_empty() {
        stub.push_str("    pass\n");
    }

    stub.push('\n');
    stub
}

/// Generate a Python stub for an enum.
fn generate_enum_stub(
    e: &vertumnus_inspector::ir::EnumItem,
    methods: &[(FunctionItem, PyO3Strategy)],
    item: &AnnotatedItem,
) -> String {
    let mut stub = String::new();

    // Doc comment
    if !e.doc.is_empty() {
        for line in e.doc.lines() {
            stub.push_str(&format!("# {}\n", line));
        }
    }

    // Manual stub warning
    if item.mapping.pyo3_strategy == PyO3Strategy::ManualStub {
        stub.push_str("# VERTUMNUS: manual binding required\n");
        for w in &item.mapping.warnings {
            stub.push_str(&format!("# WARNING: {}\n", w.message));
        }
        stub.push_str(&format!("class {}: ...\n\n", e.name));
        return stub;
    }

    let is_c_like = e.variants.iter().all(|v| v.fields.is_empty());

    if is_c_like {
        stub.push_str(&format!("class {}(IntEnum):\n", e.name));

        for variant in &e.variants {
            stub.push_str(&format!("    {} = ...\n", variant.name));
        }
    } else {
        stub.push_str(&format!("class {}:\n", e.name));

        for variant in &e.variants {
            if variant.fields.is_empty() {
                stub.push_str(&format!("    {}: \"{}\" = ...\n", variant.name, variant.name));
            } else {
                stub.push_str(&format!("    # {}: data variant\n", variant.name));
            }
        }
    }

    stub.push('\n');

    // Methods
    for (method, strategy) in methods {
        let method_stub = generate_method_stub(method, strategy);
        for line in method_stub.lines() {
            stub.push_str("    ");
            stub.push_str(line);
            stub.push('\n');
        }
        stub.push('\n');
    }

    stub.push('\n');
    stub
}

/// Generate a Python stub for a method.
fn generate_method_stub(method: &FunctionItem, strategy: &PyO3Strategy) -> String {
    let mut stub = String::new();

    // Doc comment
    if !method.doc.is_empty() {
        for line in method.doc.lines() {
            stub.push_str(&format!("# {}\n", line));
        }
    }

    if *strategy == PyO3Strategy::ManualStub {
        stub.push_str("# VERTUMNUS: manual binding required\n");
        stub.push_str(&format!(
            "def {}(self, *args: Any, **kwargs: Any) -> Any: ...\n",
            method.name
        ));
        return stub;
    }

    // Determine if this is a static method (no self)
    let has_self = method.inputs.first().map(|p| p.name.as_str()) == Some("self");

    // Build parameter list
    let mut params = Vec::new();
    if has_self {
        params.push("self".to_string());
    }
    for p in method.inputs.iter().filter(|p| p.name != "self") {
        let py_type = ir_type_to_python_type(&p.type_str);
        params.push(format!("{}: {}", p.name, py_type));
    }

    // Build return type
    let return_type = if method.output.type_str == "()" {
        "None".to_string()
    } else if *strategy == PyO3Strategy::MapErr {
        let mapped = ir_type_to_python_type(&method.output.type_str);
        if mapped.starts_with("Result[") {
            extract_python_result_ok(&mapped).unwrap_or(mapped)
        } else {
            mapped
        }
    } else if method.output.type_str == "Self" {
        "Self".to_string()
    } else {
        ir_type_to_python_type(&method.output.type_str)
    };

    stub.push_str(&format!(
        "def {}({}) -> {}: ...\n",
        method.name,
        params.join(", "),
        return_type
    ));

    stub
}

